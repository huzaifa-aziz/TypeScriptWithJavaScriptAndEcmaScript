//Logical AND
let a: boolean = true;
let b: boolean = false;
let result1: boolean = a && b; // result1 = false
let result2: boolean = true && true; // result2 = true


//Logical OR
let c: boolean = true;
let d: boolean = false;
let result3: boolean = c || d; // result3 = true
let result4: boolean = false || false; // result4 = false

//Logical NOT
let e: boolean = true;
let f: boolean = false;
let result5: boolean = !e; // result5 = false
let result6: boolean = !f; // result6 = true

