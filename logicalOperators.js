//Logical AND
var a = true;
var b = false;
var result1 = a && b; // result1 = false
var result2 = true && true; // result2 = true
//Logical OR
var c = true;
var d = false;
var result3 = c || d; // result3 = true
var result4 = false || false; // result4 = false
//Logical NOT
var e = true;
var f = false;
var result5 = !e; // result5 = false
var result6 = !f; // result6 = true
